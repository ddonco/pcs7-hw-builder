export function buildET200SPHA(rackName: string, deviceNumber: number): string {
  return `IOSUBSYSTEM 100, IOADDRESS ${deviceNumber}, "R1:6DL1 155-6AU00-0PM0" "V1.1", "${rackName}"
REDUNDANT 
BEGIN
END

IOSUBSYSTEM 100, IOADDRESS ${deviceNumber}, SLOT 0, "6DL1 155-6AU00-0PM0", "${rackName}"
AUTOCREATED 
BEGIN 
END 

IOSUBSYSTEM 100, IOADDRESS ${deviceNumber}, SLOT 0, SUBSLOT 1, "_S7H_HSP_155_6AU00_0HH0_INTERFACE_V1_1_CT", "PN-IO"
AUTOCREATED 
BEGIN 
END 

IOSUBSYSTEM 100, IOADDRESS ${deviceNumber}, SLOT 0, SUBSLOT 2, "6DL1 193-6AR00-0AA0", "Port 1 RJ45"
AUTOCREATED 
BEGIN 
END 

IOSUBSYSTEM 100, IOADDRESS ${deviceNumber}, SLOT 0, SUBSLOT 3, "6DL1 193-6AR00-0AA0", "Port 2 RJ45"
AUTOCREATED 
BEGIN 
END 

IOSUBSYSTEM 100, IOADDRESS ${deviceNumber}, SLOT 1, "6DL1 155-6AU00-0PM0", "IM155-6-PN-HA-Red"
AUTOCREATED 
BEGIN 
END 

IOSUBSYSTEM 100, IOADDRESS ${deviceNumber}, SLOT 1, SUBSLOT 1, "_S7H_HSP_155_6AU00_0HH0_INTERFACE_RED_V1_1_CT", "PN-IO-Red"
AUTOCREATED 
BEGIN 
END 

IOSUBSYSTEM 100, IOADDRESS ${deviceNumber}, SLOT 1, SUBSLOT 2, "6DL1 193-6AR00-0AA0", "Port 1 RJ45"
AUTOCREATED 
BEGIN 
END 

IOSUBSYSTEM 100, IOADDRESS ${deviceNumber}, SLOT 1, SUBSLOT 3, "6DL1 193-6AR00-0AA0", "Port 2 RJ45"
AUTOCREATED 
BEGIN 
END\n`;
}
